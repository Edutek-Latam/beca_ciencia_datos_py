#Tipo string (str)
nombre = 'Sherwin'
print(type(nombre))
print(nombre)

#Tipo entero (int)
edad = 34
print(type(edad))
print(edad)

#Tipo flotante
peso = 150.55
print(type(peso))
print(peso)

#Bool
es_dia= False
print(type(es_dia))
print(es_dia)

#Lista
meses = ['enero','febrero','marzo','abril','mayo']
print(type(meses))
print(meses)

#Tupla - inmutables-no se puede modificar despues de crearse
colores = ('rojo','verde','amarillo')
print(type(colores))
print(colores)

#Diccionarios
usuario = {'email':'paintsis@gmail.com','user':'paintsis','phone':55887799}
print(type(usuario))
print(usuario)

